
public class pattern {

	
	public static void main(String[] args) {
		int k=2,i,j;
		for(i=1;i<=5;i++){
			for(j=1;j<=i;j++){
				System.out.print(k);
				
			k=k+2;
			}
			System.out.println();
		
	}

	}
}




















































































































